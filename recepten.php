        <img class = "food_image" src="images/recepten/bosvruchten_taartje.jpg" />
            <h3>Ingredi&euml;nten bosvruchtentaartje</h3>
            <h4>Bosvruchtencremeux</h4>
            <ul>
                <li>300 gram bosvruchtenpuree</li>
                <li>255 gram suiker</li>
                <li>300 gram ei</li>
                <li>18 gram gelatine</li>
                <li>90 gram water</li>
                <li>450 gram boter (in blokjes)</li>
            </ul>
            <h4>Chocolade bretons zanddeeg</h4>
            <ul>
                <li>110 gram boter (in blokjes)</li>
                <li>150 gram bloem</li>
                <li>85 gram poedersuiker</li>
                <li>1 zakje bakpoeder</li>
                <li>1 eetlepel cacao</li>
                <li>3 eigeel</li>
                <li>40 gram pure chocolade</li>
                <li>0,50 eetlepel melk</li>
                <li>10 gram amandelmeel</li>
            </ul>
            <h4>Citroenmousse</h4>
            <ul>
                <li>9 gram gelatine</li>
                <li>45 milliliter water</li>
                <li>75 gram citroen (sap)</li>
                <li>175 gram mascarpone</li>
                <li>100 gram bosvruchtenpuree</li>
                <li>200 gram slagroom</li>
            </ul>
            <h4>Bosvruchtenspiegel</h4>
            <ul>
                <li>350 gram witte chocolade</li>
                <li>300 gram suiker</li>
                <li>150 milliliter boter, gesmolten (om het deeg mee in te smeren)</li>
                <li>19 gram gelatine</li>
                <li>120 milliliter water</li>
                <li>200 milliliter bosvruchtenpuree</li>
                <li>verse bosvruchten voor decoratie</li>
                <li>50 gram witte chocolade</li>
            </ul>
            <h3>Speciale benodigdheden</h3>
            <ul>
                <li>Bakring van 24 cm doorsnede</li>
                <li>3D siliconen vorm, 18 cm doorsnede</li>
                <li>3D siliconen vorm, 15,3 cm doorsnede</li>
            </ul>
            <h3>Werkwijze</h3>
            <h4>Bosvruchtencremeux</h4>
            <p>Meng de gelatine met het water en laat dat 10 minuten wellen.</p>
            <p>Breng de suiker met de bosvruchtenpuree aan de kook.</p>
            <p>Haal de pan van het vuur en meng dit soeplepel voor soeplepel met het ei.</p>
            <p>Pas op dat je geen roerei krijgt.</p>
            <p>Zet terug op het vuur en laat het mengsel binden.</p>
            <p>Doe de blokjes boter erbij en zodra de boter is gesmolten de gelatine.</p>
            <p>Zorg dat er geen klontjes inzitten, zeef anders de cremeux.</p>
            <p>Vul de siliconen vorm van 15,3 cm doorsnede, met de cremeux en zet in de vriezer tot de cremeux stijf bevroren is.</p>
            <p>Doe de rest van de cremeux in een schaal, dek het af met folie en laat het in de koelkast opstijven.</p>
            <h4>Chocolade bretons zanddeeg</h4>
            <p>Meng de boter, het suiker en het eigeel met een staande mixer met vlinderhaak totdat je een cr&egrave;mige bleke massa hebt.</p>
            <p>Meng daardoor 125 gram bloem, het bakpoeder en de cacao.</p>
            <p>Smelt de chocolade au bain marie met de melk, voeg toe aan het mengsel.</p>
            <p>Voeg als laatste de rest van de bloem en het amandelmeel toe en vorm daar een bal van.</p>
            <p>Als het deeg te vochtig is kun je nog eetlepels bloem toevoegen.</p>
            <p>Leg 4 uur in de koelkast om te rusten.</p>
            <p>Verwarm de oven voor op 180 graden Celsius.</p>
            <p>Bekleed een bakplaat met bakpapier en leg daarop de bakring.</p>
            <p>Rol het deeg uit tot een plak van 5 mm dik en bekleed daar de bodem van de bakring mee.</p>
            <p>Bak af in 20 minuten en laat afkoelen.</p>
            <h4>Citroenmousse</h4>
            <p>Meng de gelatine met het water en laat het 10 minuten wellen.</p>
            <p>Smelt vervolgens op laag vuur tot vloeibaar is.</p>
            <p>Meng het citroensap met de mascarpone tot een egale massa.</p>
            <p>Roer gesmolten gelatine erdoor.</p>
            <p>Meng met een flinke lepel het kookschuim en spatel dan rest van het kookschuim erdoor.</p>
            <p>Klop de slagroom tot het lobbig is en spatel dat voorzichtig door het mengsel.</p>
            <p>Vul de 3D siliconen vorm 18 cm doorsnede voor 2/3 met de citroenmousse.</p>
            <p>Haal de bevroren bosvruchtencremeux uit de siliconenhoes en druk die voorzichtig in de citroenmousse en bedek dit met de rest van de citroenmousse.</p>
            <p>Je moet de ring van cremeux niet meer kunnen zien.</p>
            <p>Bevries het geheel totdat het stijf bevroren is.</p>
            <h4>Bosvruchtenspiegel</h4>
            <p>Meng de gelatine met het water en laat 10 minuten wellen.</p>
            <p>Breng de suiker, de bosvruchtenpuree en de gecondenseerde melk langzaam aan de kook, los de gewelde gelatine erin op en vervolgens de witte chocolade.</p>
            <p>Zeef het geheel en laat het afkoelen tot 33 graden Celsius.</p>
            <p>Smelt de witte chocolade en bestrijk de bovenkant van het bretons zanddeeg ermee.</p>
            <p>Laat de chocolade weer hard worden.</p>
            <p>Haal de bosvruchtencremeux die in de koelkast stond eruit en mix het luchtig.</p>
            <p>Doe het in een spuitzak met spuitmond met ronde vorm.</p>
            <p>Haal de bevroren citroenmousse uit de siliconenvorm en giet de spiegel eroverheen tot het egaal bedekt is en een mooie laag gevormd is.</p>
            <p>Zet de ring van mousse in het midden op de bodem van bretons zanddeeg.</p>
            <p>Spuit in het midden bosvruchtencremeux en vul met verse bosvruchten.</p>
            <p>Spuit toeven bosvruchtencremeux langs de rand en garneer met blauwe bessen.</p>
