            <h3>Werkwijze</h3>
            <p>Klop het ei los verdeel in twee delen. De ene is voor het deeg, de ander voor het bestrijken.</p>
            <p>Meng boter, basterdsuiker, zelfrijzend bakmeel, het halve ei, vanillesuiker en een snufje zout tot een stevig deeg en verdeel in 3 delen. Is jouw deeg nog te droog, of juist te plakkerig? Lees dan hier hoe je er eenvoudig een mooi deeg van maakt.</p>
