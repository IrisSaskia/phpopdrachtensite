        <div id="tabs-2">
            <img class = "food_image" src="images/recepten/tompouce.jpg" />
            <h3>Ingredi&euml;nten tompoucen</h3>
            <h4>Bladerdeeg</h4>
            <ul>
                <li>200 gram patentbloem</li>
                <li>200 gram roomboter</li>
                <li>100 milliliter ijskoud water</li>
                <li>3 gram zout</li>
            </ul>
            <h4>Banketbakkersroom</h4>
            <ul>
                <li>350 milliliter melk</li>
                <li>0,50 vanillestokje</li>
                <li>85 gram suiker</li>
                <li>35 gram custardpoeder</li>
                <li>500 gram slagroom</li>
                <li>3,50 gelatine blaadjes</li>
            </ul>
            <h4>Glazuur</h4>
            <ul>
                <li>100 gram abrikozenjam</li>
                <li>200 gram fondant</li>
                <li>5 frambozen</li>
            </ul>
            <h3>Werkwijze</h3>
            <h4>Bladerdeeg</h4>
            <p>Meng bloem, zout en water en kneed snel tot een kruimelig deeg.</p>
            <p>Snijd de boter in kleine blokjes en meng door het deeg. Let op, de klontjes boter moeten zichtbaar blijven.</p>
            <p>Bestuif het werkvlak ruim met bloem en rol het deeg uit tot een rechthoekige plak.</p>
            <p>Vouw het deeg in vieren (buitenste randen naar binnen en dan het geheel dubbelvouwen), draai een kwartslag en rol opnieuw uit tot een rechthoek.</p>
            <p>Vouw het deeg wederom in vieren, verpak in plasticfolie en leg 20 minuten in de koelkast.</p>
            <p>Herhaal bovenstaande nog 2 maal.</p>
            <p>Verwarm de oven voor op 185 graden Celsius.</p>
            <p>Rol het deeg uit tot een rechthoek met een breedte van ongeveer 23 centimeter.</p>
            <p>Leg op een met bakpapier beklede bakplaat.</p>
            <p>Bak de bodem in 20 tot 30 minuten gaar. Let goed op de kleur.</p>
            <p>Als het bladerdeeg te donker wordt, dek af met bakpapier.</p>
            <p>Laat afkoelen.</p>
            <h4>Banketbakkersroom</h4>
            <p>Meng vanille, 35 gram suiker en melk door elkaar.</p>
            <p>Doe het custardpoeder in een schaaltje en schenk een scheutje melk er bij. Klop goed door tot een egaal papje.</p>
            <p>Breng melk, vanille en suiker aan de kook.</p>
            <p>Schenk een beetje warme melk bij het custardpapje, klop even door en schenk terug in de pan.</p>
            <p>Breng weer aan de kook en laat het kort pruttelen tot het ingedikt is.</p>
            <p>Stort de banketbakkersroom op een plaat en dek af met plasticfolie. Laat afkoelen.</p>
            <p>Laat de gelatine weken in ruim koud water.</p>
            <p>Knijp de gelatine uit en smelt met aanhangend water in een pannetje op laag vuur.</p>
            <p>Klop 200 gram slagroom met 20 gram suiker stijf.</p>
            <p>Klop de koude banketbakkersroom los en roer de opgeloste gelatine erdoor.</p>
            <p>Meng nu de opgeklopte slagroom erdoor.</p>
            <p>Stort de cr&egrave;me suisse op een plaat en laat ongeveer 5 minuten opstijven in de koelkast.</p>
            <p>Klop de rest van de slagroom met 30 gram suiker lobbig.</p>
            <h4>Samenstellen</h4>
            <p>Snijd het bladerdeeg netjes in twee banen van 11 centimeter breed.</p>
            <p>Doe de cr&egrave;me suisse en slagroom in spuitzakken.</p>
            <p>Spuit op de onderste plak bladerdeeg rechte banen Zwitserse room en spuit hier rechte banen slagroom op.</p>
            <p>Dek af met de tweede plak bladerdeeg.</p>
            <h4>Glazuur</h4>
            <p>Laat de abrikozenjam inkoken.</p>
            <p>Verwarm de fondant.</p>
            <p>Pers de frambozen door een zeefje boven de fondant en breng op kleur.</p>
            <p>Smeer de jam op de bovenkant van het bladerdeeg en glaceer vervolgens met lichtroze fondant.</p>
            <p>Smeer de zijkanten van de tompouce glad met een paletmes.</p>
            <p>Laat kort drogen en opstijven in de koelkast.</p>
            <p>Snijd de tompouce vervolgens in mooie kleine tompoucen van 4 centimeter breed.</p>
