            <p>Doe de helft van de appels in de vorm en strooi hier 1/3 van het kaneel-suiker mengsel overheen. De overige appelschijfjes kun je nu ook in de vorm doen, strooi de rest van het kaneel-suiker mengsel erover.</p>
            <p>Rol het laatste deel deeg uit tot een dunne lap en snij stroken van ongeveer 1 cm breed.</p>
            <p>Leg de stroken kruislings  op de appeltaart. Met wat extra deegstroken werk je de rand rondom af. Gebruik het halve ei om de stroken in te smeren.</p>
            <p>Zet de taart iets onder het midden van de oven. Bak in 60 minuten op 170 graden gaar en goudbruin.</p>
            <p>Laat de taart afkoelen in de vorm voordat je de ring verwijdert.</p>
            <p>Nog een klein beetje warm met een toef slagroom is deze taart heel erg lekker. Maar de volgende dag is hij eigenlijk n&oacute;g lekkerder, als de smaken lekker zijn doorgetrokken.</p>
